package com.harvey.spring.bean;

/**
 * 最简单的bean测试
 *
 * @author harvey
 * @version 1.0.0
 * @date 2021-01-27
 */
public class SimpleBean {

	private String name;

	private int age;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
}
